    {
        $request->validate([
            'nome' => 'required|max:20|string',
            'email' => 'required|email|min:15',
            'cep' => 'numeric|unique:App\Models\User,email',
            'senha' => 'password',
            'github' => 'url',
            'data' => 'date|timezone',
            'ip' => 'ip',
            'texto' => 'file|size:512',
            'foto' => 'image',
            'json' => 'json',
            'vivo' => 'boolean',
            'matriz' => 'distinct|nullable|present',
        ]);


    }

